#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	c;
	char			*dest;

	c = 0;
	dest = (char *)f;
	if (!s)
		return ;
	while (s[c])
	{
		f(c, &s[c]);
		c++;
	}
}
