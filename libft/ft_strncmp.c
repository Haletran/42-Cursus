#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t a;
	size_t b;

	a = 0;
	b = 0;
	while ((s1[a] != '\0' || s2[a] != '\0') && b < n)
	{
		if (s1[a] != s2[a])
			return (s1[a] - s2[a]);
		a++;
		b++;
	}
	return (0);
}
